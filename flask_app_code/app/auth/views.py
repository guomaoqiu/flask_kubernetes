@auth.route('/login', methods=['GET', 'POST'])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        user = User.query.filter_by(email=form.email.data).first()
        if user is not None and user.verify_password(form.password.data):
            login_user(user, form.remember_me.data)
            flash('登录成功','success')
            return redirect(request.args.get('next') or url_for('main.index'))
        flash('密码错误','danger')
    access_ip = request.headers.get('X-Forwarded-For',request.remote_addr)
    return render_template('auth/login.html', form=form, access_ip=access_ip)


@auth.route('/logout')
@login_required
def logout():
    logout_user()
    flash('退出登录成功.','success')
    return redirect(url_for('main.index'))


@auth.route('/register', methods=['GET', 'POST'])
def register():
    form = RegistrationForm()
    if form.validate_on_submit():
        user = User(email=form.email.data,
                    username=form.username.data,
                    password=form.password.data)
        db.session.add(user)
        db.session.commit()
        token = user.generate_confirmation_token()
        send_email(user.email, '账户确认',
                   'auth/email/confirm', user=user, token=token)
        flash('已通过电子邮件向您发送确认电子邮件.','info')
        return redirect(url_for('auth.login'))
    return render_template('auth/register.html', form=form)


